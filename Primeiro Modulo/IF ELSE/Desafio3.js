// Desafio do intervalo: Peça ao usuário um número 
// e verifique se ele está entre 50 e 100 (inclusive). 
// Exiba uma mensagem informando se o número está dentro do intervalo ou não.

const numero = 10;

if (numero >= 50 && numero <= 100) {
  console.log("O número está dentro do intervalo de 50 a 100.");
} else {
  console.log("O número está fora do intervalo de 50 a 100.");
}