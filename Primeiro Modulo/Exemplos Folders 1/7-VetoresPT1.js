// vetor dinamico
    let vetor = []; // Vetor vazio

    // Adicionando elementos ao vetor
    vetor.push(1);
    vetor.push(2);
    vetor.push(3);
    console.log(vetor); // saida: [1, 2, 3]

    // Modificando um elemento do vetor
    vetor[1] = 5;
    console.log(vetor); // saida: [1, 5, 3]

    // Removendo elementos do vetor
    vetor.pop();
    console.log(vetor); // saida: [1, 5]

    // Obtendo o tamanho do vetor
    console.log(vetor.length); // saida: 2
